import { clientCredentials } from '../utils/client';

const endpoint = clientCredentials.databaseURL;

const getListTasks = (uid) => new Promise((resolve, reject) => {
  fetch(`${endpoint}/task.json?orderBy="uid"&equalTo="${uid}"`, {
    method: 'GET',
    headers: {
      'Content-Type': 'application/json',
    },
  })
    .then((response) => response.json())
    .then((data) => {
      if (data) {
        resolve(Object.values(data));
      } else {
        resolve([]);
      }
    })
    .catch(reject);
});

const deleteTask = (firebaseKey) => new Promise((resolve, reject) => {
  fetch(`${endpoint}/task/${firebaseKey}.json`, {
    method: 'DELETE',
    headers: {
      'Content-Type': 'application/json',
    },
  })
    .then((response) => response.json())
    .then((data) => resolve(data))
    .catch(reject);
});

const getSingleTask = (firebaseKey) => new Promise((resolve, reject) => {
  fetch(`${endpoint}/task/${firebaseKey}.json`, {
    method: 'GET',
    headers: {
      'Content-Type': 'application/json',
    },
  })
    .then((response) => response.json())
    .then((data) => resolve(data))
    .catch(reject);
});

const createTask = (payload) => new Promise((resolve, reject) => {
  fetch(`${endpoint}/task.json`, {
    method: 'POST',
    headers: {
      'Content-Type': 'application/json',
    },
    body: JSON.stringify(payload),
  })
    .then((response) => response.json())
    .then((data) => {
      const setcode = { firebaseKey: data.name };
      fetch(`${endpoint}/task/${setcode.firebaseKey}.json`, {
        method: 'PATCH',
        headers: {
          'Content-Type': 'application/json',
        },
        body: JSON.stringify(setcode),
      }).then(resolve);
    })
    .catch(reject);
});

const updateTask = (payload) => new Promise((resolve, reject) => {
  fetch(`${endpoint}/task/${payload.firebaseKey}.json`, {
    method: 'PATCH',
    headers: {
      'Content-Type': 'application/json',
    },
    body: JSON.stringify(payload),
  })
    .then((response) => response.json())
    .then(resolve)
    .catch(reject);
});

export {
  updateTask,
  createTask,
  getSingleTask,
  deleteTask,
  getListTasks,
};
